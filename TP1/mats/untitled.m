function Ibin = autobin(I) % autobin but for 2 thresholds

    warning ('off','all');
    [ts,met] = multithresh(I,2);
    warning ('on','all');

    if met==0 % invalid 2nd threshold
        Ibin = double(imbinarize(I));
    else
        T = (ts(1)+ts(2))/2;
%         T = ts(2);
        Ibin = double(imbinarize(I,T));
    end

%     Ibin = double(imbinarize(I));
    
    if mean(Ibin,'all') > 0.5 % always more black
        Ibin = not(Ibin);
    end
end


